<template>
	<div id="comments-board">
		<div class="drawer-header">
			<p>{{ prefecture.prefName || "みんなはどこで働くつもりだろう？" }}</p>
		</div>
		<div class="comments">
			<PrefComment
				v-for="(comment, index) in comments"
				:key="index"
				:userComment="comment"
			/>
		</div>
		<CommentForm @emit-input-comment="postComment" />
	</div>
</template>

<script>
	import axios from "axios";
	import prefComment from "@/components/PrefComment.vue";
	import commentForm from "@/interfaces/CommentForm.vue";
	export default {
		name: "CommentsBoard",
		components: {
			PrefComment: prefComment,
			CommentForm: commentForm,
		},
		props: {
			prefecture: {
				type: Object,
				default: () => ({
					prefName: "",
					prefCode: null,
				}),
			},
			comments: {
				type: Array,
				default: () => [],
			},
		},
		methods: {
			async postComment(comment) {
				const result = await axios
					.post(`/api/comments/${this.prefecture.prefCode}`, {
						id: 1, //TODO: this.userId,
						comment: comment,
					})
					.then((response) => {
						if ((response.status = 201)) {
							/** 成功処理 */
							console.log(response);
						}
					})
					.catch((error) => {
						console.error(error);
						/**エラー処理 */
					});
				console.log(result);
			},
		},
	};
</script>
